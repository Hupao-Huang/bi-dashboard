package importutil

import "testing"

// ParseExcelDate 是 RPA 导入的关键函数 — memory feedback_rpa_date_excel 提到所有 RPA 文件
// stat_date 必须读 Excel 日期列, 不能用文件名。这个函数得 100% 覆盖。
func TestParseExcelDate(t *testing.T) {
	cases := map[string]string{
		// 标准 ISO YYYY-MM-DD
		"2026-05-10":      "2026-05-10",
		"2026-5-10":       "2026-05-10",  // 单数字月补零
		"2026-05-9":       "2026-05-09",
		"2026-1-1":        "2026-01-01",

		// 斜杠
		"2026/05/10":      "2026-05-10",
		"2026/5/10":       "2026-05-10",

		// 点分隔
		"2026.05.10":      "2026-05-10",

		// 中文
		"2026年05月10日":   "2026-05-10",
		"2026年5月10日":    "2026-05-10",
		"2026年12月":       "", // 缺日, 不合规

		// 无分隔符
		"20260510":        "2026-05-10",
		"20251231":        "2025-12-31",

		// 拼多多 MM-DD-YY 美式短年份
		"05-10-26":        "2026-05-10",
		"12-31-25":        "2025-12-31",
		"01-01-00":        "2000-01-01",  // 00 → 2000
		"12-29-69":        "2069-12-29",  // 69 → 2069 (≤69 算 20xx)
		"01-15-70":        "1970-01-15",  // 70 → 1970 (≥70 算 19xx)
		"06-15-99":        "1999-06-15",
		"13-01-25":        "",            // 月 > 12, 无效
		"00-15-25":        "",            // 月 = 0, 无效
		"05-32-25":        "",            // 日 > 31, 无效
		"05-00-25":        "",            // 日 = 0, 无效

		// 含时间, 取日期部分
		"2026-05-10 12:34:56": "2026-05-10",
		"2026/05/10 8:00":     "2026-05-10",

		// 边缘
		"":              "",
		"   ":           "",
		"abc":           "",
		"2026":          "",
		"2026-05":       "",
		"05-10":         "",
		"2026-05-10-extra": "", // 4 段, 不合规
	}

	for input, want := range cases {
		if got := ParseExcelDate(input); got != want {
			t.Errorf("ParseExcelDate(%q) = %q, want %q", input, got, want)
		}
	}
}
